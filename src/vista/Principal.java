    private void limpiarTabla(JTable listaIngredientes) {

        DefaultTableModel model = (DefaultTableModel) listaIngredientes.getModel();
        model.setRowCount(0);
    }

    public void mostrarLista(NodoIng ptr) {

        DefaultTableModel model = (DefaultTableModel) listaPlatos.getModel();
        limpiarTabla(listaPlatos);
        NodoIng p = ptr;
        while (p != null) {
            model.addRow(new Object[]{p.Plato, p.precio});
            p = p.link;
        }

    }

    public void mostrarLista(NodoPed ptr, JTable lista) {
        DefaultTableModel model = (DefaultTableModel) lista.getModel();
        limpiarTabla(lista);
        NodoPed p = ptr;
        while (p != null) {
            model.addRow(new Object[]{p.Pedido, p.cantidad, p.mesero, p.mesa});
            p = p.link;
        }

    }

