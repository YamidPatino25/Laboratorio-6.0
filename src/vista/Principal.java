    public void guardarArchivo() {
        // Crear un nuevo escritor
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(archivo))) {
            // Hallamos el numero de filas
            int filas = listaIngredientes.getRowCount();

            // Tomamos el modelo
            DefaultTableModel model = (DefaultTableModel) listaIngredientes.getModel();

            for (int i = 0; i < filas; ++i) {
                Object ingrediente = model.getValueAt(i, 0);
                Object cantidad = model.getValueAt(i, 1);

                bw.write(ingrediente + "," + cantidad);
                bw.newLine();
            }
        } catch (IOException ex) {
            // TODO enviar mensaje al usuario.
        }

    }

